                         <h1>Tips for Living With Parkinson’s Disease</h1>
                         <div class="news-detail-thumb">
                              <div class="news-image">
                                   <img src="images/yips.jpg" class="img-responsive" alt="">
                              </div>
                            
                              <p>Parkinson’s disease affects everyone differently. Whatever your case gives you, there are habits you can work into your daily routine to help you deal with your symptoms and live life more fully.</p>
                               <h3>Exercise Regularly</h3>   
                              <p>Moving and stretching your body every day will boost anyone’s health. When you have Parkinson’s, it can help give you:
                              </p>
                              <ul>
                                <li>More flexibility</li>
                                <li>Better balance</li>
                                <li>Less anxiety and depression</li>
                                <li>Improved coordination</li>
                                <li>Added muscle strength</li>
                              </ul>
                              <p>Talk to your doctor before you start any kind of physical activity. They may recommend that you team up with a physical therapist to help you find your best fitness fit. You may want to try:</p>
                              <ul>
                                <li>Walking</li>
                                <li>Swimming or water aerobics</li>
                                <li>Gardening</li>
                                <li>Stretching</li>
                                <li>Dancing</li>
                              </ul>
                              <h3>Walk Regularly</h3> 
                              <p>Balance problems can make falling a real concern when you have Parkinson’s. As you move around, especially during exercise, be smart. For instance:</p>
                              <ul>
                                <li>Plant your heel first when you take a step.</li>
                                <li>Don’t move quickly.</li>
                                <li>Work to keep your posture straight as you walk, and look ahead instead of down.</li>
                                <li>Change directions with a U-turn instead of a pivot.</li>
                                <li>Try not to carry anything when you walk.</li>
                                <li>Don’t walk backward.</li>
                              </ul>
                              <p>If, despite taking these steps, you find yourself falling, think about using a cane, walker, or other device to help you move safely.</p>
                              <h3>Sleep Well</h3>
                              <p>Sometimes, Parkinson’s can stand in the way of restful shut-eye. Parkinson's disease can cause sleep problems or abnormal dreams. Set yourself up for success by keeping good “sleep hygiene” -- patterns that will raise your chance of getting the ZZZs you need:</p>
                              <ul>
                                <li>Create a relaxing pre-bedtime routine and follow it every night.</li>
                                <li>Stick to a schedule: Go to bed and wake up at the same time every day.</li>
                                <li>Be light bright: Get plenty of natural light during the day. Avoid screens and keep your room dark at night.</li>
                                <li>Stay away from caffeine, alcohol, and exercise for at least 4 hours before bedtime.</li>
                                <li>Use your bed for sleep (and sex) only.</li>
                                <li>Keep your bedroom cool at night.</li>
                              </ul>
                              <h3>Eat for Health</h3>
                              <p>It’s common for Parkinson’s disease to come with things like bone thinning, dehydration, weight loss, and constipation. You can head off many of these symptoms if you keep close tabs on your nutrition.</p>
                              <br>
                              As you stock your pantry and plan your weekly menus, remember to:
                              <ul>
                                <li>Eat a variety of whole grains, vegetables, and fruits every day.</li>
                                <li>Watch how much fat you eat (especially the saturated kind).</li>
                                <li>Limit sugar, salt, and sodium.</li>
                                <li>Go easy on alcohol (and be sure what you do drink doesn’t interact with your meds).</li>
                                <li>Drink plenty of water -- at least 8 glasses a day.</li>
                                <li>Load up on foods packed with vitamin D, magnesium, vitamin K, and calcium for bone strength.</li>
                              </ul>
                
