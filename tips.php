                                   <h4>Be Proactive About Improving Your Quality of Life</h4>
                                   <p>The most important step you can take is to seek help right from the beginning. Education and support will help you deal with any challenges ahead. Taking action early will help you understand and deal with the many effects of the disease. A counselor or mental health care provider can design a treatment plan to meet your specific needs. The goal is to help you regain a sense of control over your life and improve your quality of life.</p>
                              </div>
                              <div class="news-author">
                                <h4>Seek Support From Others</h4>
                                <p>Friends and family can be a great source of help when you’re dealing with Parkinson’s. But sometimes, it’s a relief to be able to relate to someone who knows what it’s like to deal with the disease. In-person or online support groups can offer comfort and practical advice. They can also help you feel less alone. Ask your doctor, nurse, or social worker to suggest local or online groups you can join.

                                    Sometimes people have problems that are better addressed in a one-on-one atmosphere. By taking part in individual counseling, you may be more able to express sensitive or private feelings you have about your condition and its impact on your lifestyle and relationships.
                                    
                                    It’s common to feel depressed and anxious, too. Check in with a mental health professional if you're having a hard time enjoying life the way you used to, or if you are often angry, sad, or unlike your usual self.</p>
