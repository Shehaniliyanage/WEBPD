     <h1>Symptoms Guide </h1><br> 

     
    <p><b>1: Speech Changes  :</b> Slur words, mumble or trail off at the end of a sentence,talk slowly, but some speak rapidly, stuttering or stammering</p>
     <p><b>2: Facial Expressions changes :</b>  Loss of facial expression on both sides of the face.Eye movement reduced, like wearing a mask.</p>
     <p><b>3: Tremor at Rest -head :</b>  Head shake even when it is at rest.</p>   
     <p><b>4: Tremor at Rest-RUE :</b>  Right arm, wrist, and hand shake even when it is at rest.</p> 
     <p><b>5: Tremor at Rest-LUE :</b> Left arm, wrist, and hand shake even when it is at rest.</p> 
     <p><b>6: Tremor at Rest-RLE :</b> Right leg, ankle, and foot shake even when it is at rest.</p> 
     <p><b>7: Tremor at Rest-LLE :</b> Left leg, ankle, and foot shake even when it is at rest.</p> 
     <p><b>8: Action or Postural Tremor-RUE :</b> Right arm, wrist, and hand tremor occurs when a body part is held against gravity</p> 
     <p><b>9: Action or Postural Tremor-LUE :</b> Left arm, wrist, and hand tremor occurs when a body part is held against gravity</p> 
     <p><b>10: Rigidity-neck :</b>Inability to be to bent or be forced out of shape of neck.</p> 
     <p><b>11: Rigidity-RUE :</b> Inability to be to bent or be forced out of shape of  right arm, wrist, and hand.</p> 
     <p><b>12: Rigidity-LUE :</b> Inability to be to bent or be forced out of shape of left arm, wrist, and hand.</p> 
     <p><b>13: Rigidity-RLE:</b> Inability to be to bent or be forced out of shape of rightt leg, ankle, and foot. </p> 
     <p><b>14: Rigidity-LLE :</b> Inability to be to bent or be forced out of shape of left leg, ankle, and foot.</p> 
     <p><b>15: Finger Taps-RUE :</b> Taps thumb with index finger of right hand in rapid succession</p> 
     <p><b>16: Finger Taps-LUE :</b> Taps thumb with index finger of left hand in rapid succession</p> 
     <p><b>17: Hand Movements -RUE :</b> Open and close right hand in rapid succesion.</p> 
     <p><b>18: Hand Movements -LUE :</b> Opens and close left hand in rapid succesion.</p> 
     <p><b>19: Rapid Alternating Movements-RUE :</b> Pronation-supination movements of  right hand, vertically and horizontally, with as large an amplitude as possible, both hands simultaneously.</p> 
     <p><b>20: Rapid Alternating Movements-LUE :</b> Pronation-supination movements of  left hand, vertically and horizontally, with as large an amplitude as possible, both hands simultaneously.</p> 
     <p><b>21: Leg Agility -RLE :</b>  Tap heel on the ground in rapid succession picking up entire  right leg. Amplitude should be at least 3 inches</p> 
     <p><b>22: Leg Agility -LLE :</b>  Tap heel on the ground in rapid succession picking up entire left leg. Amplitude should be at least 3 inches</p> 
     <p><b>23: Arising from Chair :</b> Attempt to rise from a straightbacked chair, with arms folded across chest</p> 
     <p><b>24: Posture :</b> Stooped or rounded shoulders, decreased low-back curve or forward lean of the head or whole body, making you look hunched over</p> 
     <p><b>25: Gait :</b> Less steady walk that arises from changes in posture, slowness of movement (bradykinesia) and a shortened stride.</p> 
     <p><b>26: Postural Stability :</b> Response to sudden, strong posterior displacement produced by pull on shoulders while  erect with eyes open and feet slightly apart.</p> 
     <p><b>27: Body Bradykinesia and Hypokinesia :</b> Combining slowness, hesitancy, decreased armswing, small amplitude, and poverty of movement in general.</p> 
     
             
     
 
